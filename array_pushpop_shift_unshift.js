
//push
var numbers = [20, 30, 40, 50, 60];
numbers.push(70);//posh will add add a value inside the array on the last  position 
console.log(numbers);


//pop
var numbers2 = [20, 30, 40, 50, 60, 70];
numbers2.pop();// pop will vanish the last value of array.
console.log(numbers2);

//unshift
var numbers3 = [20, 30, 40, 50, 60];
numbers3.unshift(10);//unshift will add the given value inside the array at the position 0/first of the array
console.log(numbers3);


//shift
var numbers4 = [20, 30, 40, 50, 60];
numbers4.shift();//shift will remove the the first value of array
console.log(numbers4);
